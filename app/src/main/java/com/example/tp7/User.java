package com.example.tp7;
public class User {
    public String name, surname, age,  illness, email, role;
    public User() {}
    public User(String name, String surname, String age, String illness,  String email, String role) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.illness = illness;

        this.email = email;
        this.role = role;
    }
}
